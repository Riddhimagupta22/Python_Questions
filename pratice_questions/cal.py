# square_root of number..
num1 = int(input("Enter the value of num1 :\n"))
num  = num1**0.5
print ("The square root of the given number is :",num)
print("square root of a number is done.\n")

# area of triangle..
base = float(input("Enter the value of base :\n")) 
height = float(input("Enter the value of height :\n")) 
area = 0.5*base*height
print ("Area of Triangle :",area)
print("Area of Triangle is done.\n")

# area of Circle..
radius = float(input("Enter the value of radius :\n")) 
pie = float(input("Enter the value of pie :\n")) 
area = pie*radius*radius
print ("Area of Circle :",area)
print("Area of Circle is done.\n")