# convert kilometer to mile

var = float(input("Enter the value(in km) : "))
mile = var*0.62
print ("Kilometer to miles :",mile,"miles")

print("Done Successfully!\n")

# convert celsius to fahreneit

num = int(input("Enter value of Temperature(in celsius) : "))
temp = (num*(9/5))+32
print ("Celsius to Fahreneit :",temp,"Fahreneit")

print("Done Successfully!\n")